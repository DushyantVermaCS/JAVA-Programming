public class DataTypes{
    public static void main(String args[]){
        byte x = 20;   //can store small integer values to save memory...

        int a = 45;

        float rate = 2.3f; // In float it is necessasory to add f after any digit
        
        double price = 3.125;

        boolean isThisCodeCool = true;

        char myCharacte = '@'; //Character always store in single quotes...
        
        System.out.println(x);
        System.out.println(price);
        System.out.println(rate);
        System.out.println(a);
        System.out.println(isThisCodeCool);
        System.out.println(myCharacte);
    }
}